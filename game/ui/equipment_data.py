"""Static equipment catalog data derived from design guidance."""
from __future__ import annotations

from typing import Dict, List, Sequence


EquipmentDefinition = Dict[str, object]


def _weapon(
    *,
    id: str,
    name: str,
    ship_class: str,
    price: int,
    durability: int,
    stats: Dict[str, float],
    upgrades: Sequence[str],
    description: str,
    tags: Sequence[str] = (),
) -> EquipmentDefinition:
    return {
        "id": id,
        "name": name,
        "slot_family": "weapon",
        "ship_class": ship_class,
        "level": 1,
        "durability": durability,
        "durability_max": durability,
        "price": price,
        "stats": stats,
        "upgrades": tuple(upgrades),
        "description": description,
        "tags": tuple(tags),
    }


def _module(
    *,
    id: str,
    name: str,
    slot_family: str,
    ship_class: str,
    price: int,
    durability: int,
    stats: Dict[str, float],
    upgrades: Sequence[str],
    description: str,
    tags: Sequence[str] = (),
) -> EquipmentDefinition:
    return {
        "id": id,
        "name": name,
        "slot_family": slot_family,
        "ship_class": ship_class,
        "level": 1,
        "durability": durability,
        "durability_max": durability,
        "price": price,
        "stats": stats,
        "upgrades": tuple(upgrades),
        "description": description,
        "tags": tuple(tags),
    }


